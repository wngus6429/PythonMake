# plus, minus, times, division, negation, power
# 계산기 만들기


def plus(a, b):
    return print(a + b)


def minus(a, b):
    print(a - b)


def times(a, b):
    return print(a * b)


def division(a, b):
    return print(a / b)


def negation(a):
    return print(-a)


def power(a, b):
    return print(a ** b)


def remainder(a, b):
    return print(a % b)


